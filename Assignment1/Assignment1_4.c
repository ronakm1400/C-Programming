/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//											     
// 			Problem Statement : Accept one number and check whether number is divisible by 5 or not   
//											     
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

typedef int BOOL;
#define TRUE 1
#define FALSE 0

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//							     
// 			Function Name : Check				     
// 			Input : Accepts one integer				     
// 			Output : Returns boolean value			     
// 			Description : returns TRUE if number is divisible by 5    
// 		 				  returns FALSE if number is divisible by 5   
// 			Author : Ronak Sunil Mutha				     
// 			Date : 23.04.2023					     
//							     		
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

BOOL Check(int iNo)
{
	if ((iNo%5) == 0)
	{
		return TRUE;
	}
	else 
	{
		return FALSE;
	}
	
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//										
//			Entry point function main						
//														
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	
	auto signed int iValue = 0;
	
	BOOL bRet = FALSE;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);
	
	bRet = Check(iValue);
	
	if (bRet == TRUE)
	{
		printf("Divisible by 5\n");
	}
	else
	{
		printf("Not Divisible by 5\n");
	}

	return 0;

}
