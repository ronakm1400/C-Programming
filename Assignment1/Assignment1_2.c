///////////////////////////////////////////////////////////////////////////////////////////////
//									    
//		 Problem Statement : Program to print 5 times "Marvellous" on console     
//									    
//////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////
//								
// 		Function Name : Display					
// 		Input : Accepts nothing					
// 		Output : Returns nothing					
// 		Description : Displays "Marvellous" on Console 5 times	
// 		Author : Ronak Sunil Mutha					
// 		Date : 23.04.2023						
//								
//////////////////////////////////////////////////////////////////////////////////////////////

void Display()
{
	register int iCnt = 0;
	
	for(iCnt=1; iCnt<=5; iCnt++)
	{
		printf("Marvellous...\n");
	}

}

/////////////////////////////////////////////////////////////////////////////////////////////
//														
// 			Entry point function main								
//														
/////////////////////////////////////////////////////////////////////////////////////////////


int main(int argc , char *argv[])
{
	Display();
	
	return 0;

}
