//////////////////////////////////////////////////////////////////
//								
// 		Problem Statement : Program to print 5 to 1 on console	
//								
//////////////////////////////////////////////////////////////////

#include<stdio.h>

//////////////////////////////////////////////////////////////////
//								
// 		Function Name : Display					
// 		Input : Accepts nothing					
// 		Output : Returns nothing					
// 		Description : Displays 5 to 1 on Console 			
// 		Author : Ronak Sunil Mutha					
// 		Date : 23.04.2023						
//								
//////////////////////////////////////////////////////////////////

void Display()
{
	register int iCnt = 0;
	
	for (iCnt = 5; iCnt>=1; iCnt--)
	{
		printf("%d\n",iCnt);
	}

}
//////////////////////////////////////////////////////////////////
//											
// 			Entry point function main							
//													
//////////////////////////////////////////////////////////////////

int main(int argc , char *argv[])
{
	
	Display();
	
	return 0;

}
