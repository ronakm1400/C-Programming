void strrevX(char *Str)
{
	char *cEnd = NULL;
	char cTemp = '\0';
	
	cEnd = Str;
		
	while (*cEnd != '\0')
	{
	 	cEnd = cEnd + 1;
	}
	cEnd = cEnd - 1;
	
	while (Str < cEnd)
	{
		cTemp = *Str;
		*Str = *cEnd;
		*cEnd = cTemp;
		
		Str++;
		cEnd--;
	}

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//		Entry point function main	
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc,char *argv[])
{

	auto char cArr[25] = {'\0'};

	printf("Enter String : ");
	scanf("%[^'\n']s",cArr);
	
	strrevX(cArr);
	
	printf("Reverse String is : %s\n",cArr);
	
	return 0;

}

