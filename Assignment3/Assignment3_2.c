#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//		      						         		
//       		Function Name : DisplayFactors			     		 
//       		Input : Accepts one integer				     	 
//       		Output : Return type void 				     	 
//       		Description : Prints all factors of a number in 	     	 	
//	         	 	   decreasing order	                     	 
//       		Author : Ronak Sunil Mutha				     	 
// 	     		Date : 04.05.2023					     	 	
//	
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


void DisplayFactors(int iNo)
{

	auto int iCnt = 0;
	
	if(iNo<0)
	{
		iNo = -iNo;
	}
	
	for (iCnt = (iNo/2); iCnt>=1; iCnt--)
	{
		if((iNo%iCnt) == 0)
		{
			printf("Factors of %d are : %d\n",iNo,iCnt);
		}
	
	}

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//											
//										Entry Point Function main				
//									
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc , char *argv[])
{
	
	auto int iValue = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);
	
	DisplayFactors(iValue);	

	return 0;

}
