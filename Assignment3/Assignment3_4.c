//											
// 				Problem Statement : Write a program which accepts number from user and returns sum of 
//									its non factors numbers      
//									
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 		
//       		Function Name : SumNonFact			     		 	
//       		Input : Accepts one integer				     	 
//       		Output : Return type integer				     	 
//       		Description : return summation of all non factors numbers       
//       		Author : Ronak Sunil Mutha				     	 
// 	 			Date : 04.05.2023					     	 	
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int SumNonFact(int iNo)
{
	auto int iSum = 0;
	auto int iCnt = 0;
	
	if(iNo<0)
	{
		iNo = -iNo;
	}
	
	for (iCnt = 1; iCnt<=(iNo); iCnt++)
	{
		if((iNo%iCnt) != 0)
		{
			iSum = iSum + iCnt;
		}

	}
	return iSum;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//										Entry Point Function main 				
//									
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc , char *argv[])
{
	
	auto int iValue = 0;
	auto int iRet = 0;	
		
	printf("Enter Number : ");
	scanf("%d",&iValue);
	
	iRet = SumNonFact(iValue);	
	printf("Summation of all non-factor number is : %d \n",iRet);
	
	return 0;

}

// Time Complexity : O(N)
