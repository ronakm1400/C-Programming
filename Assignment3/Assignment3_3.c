//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//									
//				 Problem Statement : Write a program which accepts number from user and display its non factors		   	
//								
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//		      						         
//       		Function Name : DisplayFactors			     		 	
//       		Input : Accepts one integer				     	 
//       		Output : Return type void 				     	 
//       		Description : Prints all non factors of a number                
//       		Author : Ronak Sunil Mutha				     	 
// 	 			Date : 04.05.2023					     	 
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DisplayNonFact(int iNo)
{

	auto int iCnt = 0;
	
	if(iNo<0)
	{
		iNo = -iNo;
	}
	
	for (iCnt = 1; iCnt<=(iNo); iCnt++)
	{
		if((iNo%iCnt) != 0)
		{
			printf("Non-Factors of %d are : %d\n",iNo,iCnt);
		}
	
	}

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//									
//											Entry Point Function main				
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc , char *argv[])
{
	
	auto int iValue = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);
	
	DisplayNonFact(iValue);	

	return 0;

}

// Time Complexity : O(N)
