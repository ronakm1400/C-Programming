/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Problem Statement : Write a program which displays ASCII table . Table contains symbol , Decimal ,
//					Hexadecimal and Octal representataion of every member from 0 to 255.	
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name   : DisplayASCII
//		Input	      	: Accepts nothing
//		Output		: returns nothing
//		Description	: This function displays all the ascii values of decimal,character,hexadecimal,octal
//		Author Name	: Ronak Sunil Mutha
//		Date 		: 20/05/2023
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DisplayASCII()
{
	register int iCnt = 0;

	printf("\nDecimal \tCharacter\tHexadecimal\tOctal\n");
	for (iCnt = 0; iCnt <= 255; iCnt++)	
	{
		printf("\n%d \t\t%c\t\t%x\t\t%o\n",iCnt,iCnt,iCnt,iCnt);
	}	

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//		Entry point function main
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc,char *argv[])
{	
	DisplayASCII();

	return 0;
}
