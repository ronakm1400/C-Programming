#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////
//											    
//	      										    
//	      Function Name : Table							    
//	      Input 		: Accepts one integer  					    	      	
//	      Output 		: returns nothing						      	     	
//	      Description 	: Displays table of given number				      
//	      Author 		: Ronak Sunil Mutha						    
//	      Date 			: 04/05/2023	                					       
//											    
//////////////////////////////////////////////////////////////////////////////////////////////

void Table(int iNo)
{
	auto int iTable = 0;
	register int iCnt = 0;
	
	if ( (iNo) < 0)
	{
		iNo = -iNo;
	}
	
	for (iCnt = 1; iCnt <= 10; iCnt++)
	{
		iTable = iNo * iCnt;
		printf("%d\t",iTable);
	}
	printf("\n");

}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//				Entry Point Function Main 				    		
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	
	auto int iValue = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);
	
	Table(iValue);	
	
	return 0;

}
