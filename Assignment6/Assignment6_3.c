#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//											      
//	      Function Name 	: Factorial							      	
//	      Input 			: Accepts one integer  					    	      	
//	      Output 			: returns integer							      
//	      Description 		: Displays factorial of given number				      
//	      Author 			: Ronak Sunil Mutha						      	
//	      Date 				: 04/05/2023	                					      	
//											       
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


int Factorial(int iNo)
{
	auto int iFact = 1;
	register int iCnt = 0;
	
	if( (iNo) < 0 )
	{
		iNo = -iNo;
	}
	
	for (iCnt = iNo; iCnt >= 1; iCnt--)
	{
		iFact = iFact * iCnt;	
	}
	return iFact;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//				Entry Point Function Main 				    		
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	
	auto int iValue = 0;
	auto int iRet = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);
	
	iRet = Factorial(iValue);
	printf("%d factorial is : %d\n",iValue,iRet);
	
	return 0;

}
