/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name	 : DisplayReverse
//		Input		 : Accepts two character array
//		Output		 : Returns nothing
//		Description	 : This function displays the reversed string 
//		Author		 : Ronak Sunil Mutha		
//		Date 		 : 20/05/2023
//
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DisplayReverse(char *Original , char *Reversed)
{
	auto signed int iLength = 0;
	
	while (*Original != '\0')
	{
		Original++;
		iLength++;	
	}
	Original--;
	
	while (iLength != 0)
	{
		*Reversed = *Original;
		Original--;
		Reversed++;
		iLength--;		
	}
	Reversed = '\0';	

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//		Entry point function main
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc,char *argv[])
{

	auto char cArr[25] = {'\0'},cBrr[25] = {'\0'};
		
	printf("Enter String : ");
	scanf("%[^'\n']s",cArr);
		
	DisplayReverse(cArr,cBrr);
	
	printf("Reversed string of '%s' is : %s\n",cArr,cBrr);	
		
	return 0;

}
