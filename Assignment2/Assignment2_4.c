//											     
//			 Problem Statement : Accept two numbers from user and display first number in second number of times                                       
//									
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//							     
// 			 						Function Name : Display				     	
// 			 						Input : Accepts two integer				     
// 			 						Output : Returns nothing 				     
// 			 						Description : Displays first number in second number of times on screen 	     	
// 	     	 		       			Author : Ronak Sunil Mutha				     
//									Date : 23.04.2023					     
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo,int iFrequency)
{
	auto int iCnt = 0;
	
	for (iCnt=1; iCnt<=iFrequency; iCnt++)
	{
		printf("%d ",iNo);	
	}

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//								
// 										 Entry point function main									
//												
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc , char *argv[])
{

	auto int iValue = 0;
	auto int iCount = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);
	
	printf("Enter Frequence : ");
	scanf("%d",&iCount);
	
	Display(iValue,iCount);

	return 0;

}