#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	 Function Name 	: MultDigits
//	 Input 			: Accepts one integer
//	 Output 		: Returns integer
//	 Description 	: Performs multiplication of all digits in number 
//	 Author Name 	: Ronak Sunil Mutha
//	 Date 			: 10/05/2023
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int MultDigits(int iNo)
{
	auto signed int iMult = 1;
	auto signed int iDigit = 0;
	
	if(iNo < 0)
	{
		iNo = -iNo;
	}
	
	while (iNo != 0)
	{
		iDigit = iNo % 10;
		
		if( iDigit == 0 )
		{
			iDigit = 1;	
		}
		
		iMult = iMult * iDigit;	
		
		iNo = iNo / 10;
	}
	return iMult;

}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//				Entry Point Function Main 				    		
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc, char *argv[])	
{

	auto signed int iValue = 0;
	auto signed int iRet = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);

	iRet = MultDigits(iValue);
	printf("Multiplication is : %d \n",iRet);

	return 0;

}
