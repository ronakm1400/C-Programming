void struprX(char *Str)
{
	
	while (*Str != '\0')
	{
		if ( (*Str >= 'a') && (*Str <= 'z') )
		{
			*Str = *Str - CONST;	
		}
		else if ( (*Str >= 'A') && (*Str <= 'Z') )
		{
			*Str = *Str + CONST;
		}
		Str++;
	}

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//		Entry point function main
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc,char *argv[])
{

	auto char cArr[25] = {'\0'};
	
	printf("Enter String : ");
	scanf("%[^'\n']s",cArr);
	
	struprX(cArr);
	
	printf("Toggled case is : %s\n",cArr);
	
	return 0;

}
