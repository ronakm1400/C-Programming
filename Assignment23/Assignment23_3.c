#include<stdio.h>

#define CONST 32

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name  	  : strtoggleX
//		Input		  : character array		
//		Output		  : returns nothing
//		Description	  : This function accepts string from user and toogle the case	
//		Author Name	  : Ronak Sunil Mutha	
//		Date		  : 20/05/2023
