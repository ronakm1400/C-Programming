#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name 	  : CountWhiteSpaces
//		Input		  : Character array
//		Output		  : returns integer
//		Description	  : This function counts number of whitespaces in given string
//		Author Name	  : Ronak Sunil Mutha	
//		Date		  : 20/05/2023
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int CountWhiteSpaces(char *Str)
{
	auto signed int iCount = 0;

	while (*Str != '\0') 
	{
		if (*Str == ' ')
		{
			iCount++;
		}
		Str++;
	}
	return iCount;

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//		Entry point function main
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc,char *argv[])
{
	auto char cArr[25] = {'\0'};
	auto int iRet = 0;
	
	printf("Enter String : ");		
	scanf("%[^'\n']s",cArr);
	
	iRet = CountWhiteSpaces(cArr);
	printf("White Spaces in string are : %d\n",iRet);
	
	return 0;

}
