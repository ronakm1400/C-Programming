//		Problem Statement  : Write a program which accepts string from user and converts it into lower case.
//						
//						Input	  : "MARVELLOUS"
//						Output	  : "marvellous"						
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

#define CONST 32

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name  	  : strlwrX
//		Input		  : character array		
//		Output		  : returns nothing
//		Description	  : This function accepts string from user and converts it into lower case	
//		Author Name	  : Ronak Sunil Mutha	
//		Date		  : 20/05/2023
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void strlwrX(char *Str)
{
	
	while (*Str != '\0')
	{
		if ( (*Str >= 'A') && (*Str <= 'Z') )
		{
			*Str = *Str + CONST;	
		}
		Str++;
	}

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//		Entry point function main
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc,char *argv[])
{

	auto char cArr[25] = {'\0'};
	
	printf("Enter String : ");
	scanf("%[^'\n']s",cArr);
	
	strlwrX(cArr);
	
	printf("String in lower case is : %s\n",cArr);
	
	return 0;

}
