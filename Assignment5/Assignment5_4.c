//											       	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//											      
//		Function Name 		: OddDisplay						      							
//		Input 				: accepts one integer						      
//		Output 				: returns nothing						      
//		Description 		: Prints odd numbers upto N					      	
//		Author 				: Ronak Sunil Mutha						      
//		Date 				: 04/05/2023							      	
//											      	
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void OddDisplay(int iNo)
{
	register int iCnt = 0;
	
	for(iCnt = 1; iCnt < iNo; iCnt++)
	{
		if((iCnt % 2) != 0 )
		{
			printf("%d\t",iCnt);
		}
	}
	
	printf("\n");
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//				Entry Point Function Main 				    		
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	auto int iValue = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);
	
	OddDisplay(iValue);
	
	return 0;

}

// Time Complexity : O(N)
