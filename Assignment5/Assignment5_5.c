//	Problem Statement : Write a program which accepts N number and print first 5 multiples of N number					      
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//											    
//	  		Function Name 	: MultipleDisplay					            
//			Input 			: Accepts one integer				     	  	    
//       	Output 			: Returns nothing				     	            
//       	Description 	: Displays first 5 multiples on screen                          
//       	Author		 	: Ronak Sunil Mutha				     	 	    
// 	 		Date 			: 04.05.2023					     		    	
//											    
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void MultipleDisplay(int iNo)
{
	auto int iMultiple = 0;
	register int iCnt = 0;
	
	for (iCnt = 1; iCnt <= 5; iCnt++)
	{
		iMultiple = iNo * iCnt;	
		printf("%d\t",iMultiple);			
	}
	printf("\n");

}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//				Entry Point Function Main 				    		
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	auto int iValue = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);
	
	MultipleDisplay(iValue);

	return 0;
}
