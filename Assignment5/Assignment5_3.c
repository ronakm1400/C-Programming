#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//											    			
//	  		Function Name 	: Display
//       	Input 			: Accepts one integer				     	  	    
//       	Output 			: Returns nothing				     	            
//       	Description 	: Displays number line on screen			  	    
//       	Author 			: Ronak Sunil Mutha				     	 	    
// 	 		Date 			: 04.05.2023					     		    
//											    
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
	auto int iCnt = 0;
	
	for (iCnt = iNo; iCnt >= 1; iCnt--)
	{
		printf("%d\t",-iCnt);
	}
		
	for(iCnt = 0; iCnt <= iNo; iCnt++)
	{
		printf("%d\t",iCnt);
	}
	printf("\n");
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//				Entry Point Function Main 				    		
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{

	auto int iValue = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);
	
	Display(iValue);

	return 0;
}
