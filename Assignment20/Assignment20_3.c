/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Problem Statement : Accept Character from user and check whether it is digit or not
//					(0-9)
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

typedef int BOOL;

#define TRUE 1
#define FALSE 0

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name 	: CheckAlphabet
//		Input 		: Accepts one character
//		Output		: returns Boolean value
//		Description	: This function accepts a single charachter from user and checks whether it is 
//					digit or not. Returns true if it is an alphabet else returns false
//		Author Name 	: Ronak Sunil Mutha
//		Date		: 20/05/2023
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////


BOOL CheckAlphabet(char cVal)
{

	if  ( (cVal >= '0') && (cVal <= '9') ) 
	{
		return TRUE;
	}
	else
	{
		return FALSE;
	}

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//		Entry point function main
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc,char *argv[])
{

	auto char ch = '\0';
	BOOL bRet = FALSE;

	printf("\nEnter digit : ");
	scanf("%c",&ch);
	
	bRet = CheckAlphabet(ch);
	
	if(bRet == TRUE)
	{
		printf("\n%c is digit\n",ch);
	}
	else
	{
		printf("\nIt is not an digit\n");
	}
	

	return 0;

}
