//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//											  	
//	Function Name 		: Count 								  
//	Input 				: Accepts one integer							  
//	Output 				: Return type integer							  
//	Description 		: Counts the frequency of digits less than 6 and returns the frequency		  						  
//	Author Name 		: Ronak Sunil Mutha							  
//	Date 				: 10/05/2023								  
//											  
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int Count(int iNo)
{
	auto signed int iDigit = 0;
	auto int iCnt = 0;
	
	if (iNo < 0)
	{
		iNo = -iNo;
	}
	
	while (iNo != 0)
	{
		iDigit = iNo % 10;
		
		if (iDigit < 6)
		{
			iCnt++;
		}
		iNo = iNo / 10;
	}
	return iCnt;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//				Entry Point Function Main 				    		
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	auto signed int iValue = 0;
	auto int iRet = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);		
	
	iRet = Count(iValue);
	
	printf("Frequency of digits less than 6 in %d is : %d\n",iValue,iRet);
	
	return 0;

}
