#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function Name 		: Count Four							  
//	Input 				: Accepts one integer							  
//	Output 				: Return type integer							  
//	Description 		: Counts the frequency of digit 4 and returns the frequency		  	
//	Author Name 		: Ronak Sunil Mutha							  
//	Date 				: 10/05/2023								  
//											  
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int CountFour(int iNo)
{
	auto signed int iDigit = 0;
	auto int iCnt = 0;
	
	if (iNo < 0)
	{
		iNo = -iNo;
	}
	
	while (iNo != 0)
	{
		iDigit = iNo % 10;
		
		if (iDigit == 4)
		{
			iCnt++;
		}
		iNo = iNo / 10;
	}
	return iCnt;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//				Entry Point Function Main 				    		
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(int argc,char *argv[])
{
	auto signed int iValue = 0;
	auto int iRet = 0;
	
	printf("Enter Number : ");
	scanf("%d",&iValue);		
	
	iRet = CountFour(iValue);
	
	printf("Frequency of digit 4 in %d is : %d\n",iValue,iRet);
	
	return 0;

}
